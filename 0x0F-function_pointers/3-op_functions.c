#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>
/**
 * op_add - fuction for sum
 * @a: input value
 * @b: input value
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function for subtraction
 * @a: input value
 * @b: input value
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_div - function for division
 * @a: input value
 * @b: input value
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mul - function for multiplication
 * @a: input value
 * @b: input value
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_mod - function for modulus
 * @a: input value
 * @b: input value
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
